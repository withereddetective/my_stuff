#include <iostream>
using namespace std;

int main(){

    // for-loop runs 10 times
    for (int i = 1; i <= 10; i++){
        
        // print i
        cout << i << endl;

    }

    // print blank line
    cout << endl;
    
    // for-loop runs 10 times but counts backwards this time
    for (int i = 10; i >= 1; i--){
        
        // print i
        cout << i << endl;

    }
    

}